package students;

public class Student {
	
	private String name;
	private String gender;
	public int getGrade() {
		return grade;
	}
	
	private int grade;
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public void setGrade(int grade) {
		this.grade = grade;
	}

	
}
